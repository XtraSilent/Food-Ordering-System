     */
    public function store(Request $request)
    {
        $this->validate(request(),[
            'cuisine_id' => 'required',
            'user_id' => 'required',
            'status_id' => 'required',
            'quantity' => 'required',
            'totalprice' => 'required',
        ]);
        Order::create([
            'cuisine_id'=>request('cuisine_idname'),
            'user_id'=>request('user_id'),
            'status_id'=>request('status_id'),
            'quantity'=>request('quantity'),
            'totalprice'=>request('totalprice')
            
        ]);
        return redirect()->route('home')->with('success','Success Send to Server!');
    }

